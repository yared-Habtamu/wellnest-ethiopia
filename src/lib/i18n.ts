const LANG_KEY = "wellnest.lang";
let initial: "en" | "am" = "en";
if (typeof window !== "undefined") {
  try {
    const v = window.localStorage.getItem(LANG_KEY);
    if (v === "am" || v === "en") initial = v;
  } catch {
    /* ignore */
  }
}

if (!i18n.isInitialized) {
  void i18n.use(initReactI18next).init({
    resources: {
      en: { translation: en },
      am: { translation: am },
    },
    lng: initial,
    fallbackLng: "en",
    interpolation: { escapeValue: false },
    returnObjects: true,
  });
}

export function setLanguage(lng: "en" | "am") {
  void i18n.changeLanguage(lng);
  try {
    window.localStorage.setItem(LANG_KEY, lng);
  } catch {
    /* ignore */
  }
}

export function getLanguage(): "en" | "am" {
  return (i18n.language as "en" | "am") || "en";
}

export default i18n;